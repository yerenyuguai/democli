export const mixin={
    methods: {
        showName(){
            alert(this.name);
        }
    },
}
export const mixin2={
    data() {
        return {
            x:100,
            y:200
            //如果组件里面没有的，用这里的，如果组件里的有的，就用组件的
        }
    },
}
