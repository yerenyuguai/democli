//两个类似的页面的，如果写成一个，复用需要很多判断逻辑，虽然看似代码量少了，但是后期维护会很乱
//如果写成两个组件，相同的代码用mixin维护，其他逻辑各写各的，对于后期维护就非常清晰容易

